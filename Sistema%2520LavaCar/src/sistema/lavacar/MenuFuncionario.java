    private void nameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nameActionPerformed
        Customer p = c.procuraPessoa(name.getText());
        name.setEditable(false);

        if(p == null) //Cadastrar
        {
            /* Permite ao usuário inserir informações em
            todos os campos */
            this.rg.setEditable(true);
            this.cpf.setEditable(true);
            this.birth.setEditable(true);
            this.address.setEditable(true);
            this.phone1.setEditable(true);
            this.phone2.setEditable(true);

            this.brand.setEditable(true);
            this.board.setEditable(true);
            this.model.setEditable(true);
            this.year.setEditable(true);
            this.color.setEditable(true);
            this.description.setEditable(true);
        }
        else //Editar
        {
            /* Imprime as informações guardadas no sistema
            Permitindo editar apenas alguns campos */
            this.rg.setText(p.getRg());
            this.cpf.setText(p.getCpf());
            this.address.setText(p.getAddress());
            this.birth.setText(p.getDateOfBirth());
            this.phone1.setText(p.getPhone1());
            this.phone2.setText(p.getPhone2());
            this.brand.setText(p.getVehiclesOfCustomer().get(0).getBrand());
            this.board.setText(p.getVehiclesOfCustomer().get(0).getBoard());
            this.model.setText(p.getVehiclesOfCustomer().get(0).getModel());
            this.year.setText(p.getVehiclesOfCustomer().get(0).getYear());
            this.color.setText(p.getVehiclesOfCustomer().get(0).getColor());
            this.description.setText(p.getVehiclesOfCustomer().get(0).getDescription());
            int size = p.getVehiclesOfCustomer().get(0).getSize();
            if(size == 1)
                pequeno.doClick();
            else if(size==2)
                medio.doClick();
            else //size == 3
                grande.doClick();
            
            this.address.setEditable(true);
            this.phone1.setEditable(true);
            this.phone2.setEditable(true);
        }
    }//GEN-LAST:event_nameActionPerformed

