    private void adicionarNaFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarNaFilaActionPerformed
        
        Vehicle v = c.procuraVeiculo(boardFila.getText());
        if(v == null) //Não está cadastrado no sistema
        {
            JOptionPane.showMessageDialog(null, "Veículo não cadastrado!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else //Tudo ok
        {
            /* Adiciona no sistema, na interface e atualiza */
            c.getService().getFila().add(v);
            listModel.add(0, boardFila.getText());
            list.setModel(listModel);
        }
    }//GEN-LAST:event_adicionarNaFilaActionPerformed

    private void removerDaFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerDaFilaActionPerformed
        //Se não tem nenhum item selecionado
        if(list.getSelectedIndex() == -1)
            JOptionPane.showMessageDialog(null, "Posição inválida. Selecione um item"
                    + " para remover da lista", "Error", JOptionPane.ERROR_MESSAGE);
        else //Se tem algum selecionado
        {
            c.getService().getFila().remove(list.getSelectedIndex());
            listModel.remove(list.getSelectedIndex());
        }
    }//GEN-LAST:event_removerDaFilaActionPerformed

    private void lavarFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lavarFilaActionPerformed
        //Se a lista está vazia
        if(list.getModel().getSize() == 0)
            JOptionPane.showMessageDialog(null, "Adicione veículos na fila para"
                    + "lavá-los!", "Error", JOptionPane.ERROR_MESSAGE);
        else //Se tem veículos na fila
            c.getService().executar();
        //tem que remover do sistema e da interface
    }//GEN-LAST:event_lavarFilaActionPerformed
        
