    private void board2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_board2ActionPerformed
        if(owner.getText().equals("") || board2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Digite todas as informações para "
                    + "continuar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Customer p = c.procuraPessoa(owner.getText());
        if(p == null)
        {
            JOptionPane.showMessageDialog(null, "Cliente não cadastrado! Use a aba "
                    + "'cliente' para efetuar o cadastro antes de adicionar ou editar "
                    + "um veículo!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Vehicle v = c.procuraVeiculo(board2.getText());
        owner.setEditable(false);
        board2.setEditable(false);
        
        if(v == null) //Cadastrar
        {          
            /* Permite ao usuário inserir informações em
            todos os campos */
            this.brand2.setEditable(true);
            this.board2.setEditable(true);
            this.model2.setEditable(true);
            this.year2.setEditable(true);
            this.color2.setEditable(true);
            this.description2.setEditable(true);
        }
        else //Editar
        {
            /* Imprime as informações guardadas no sistema
            Permitindo editar apenas alguns campos */
            this.brand2.setText(v.getBrand());
            this.board2.setText(v.getBoard());
            this.model2.setText(v.getModel());
            this.year2.setText(v.getYear());
            this.color2.setText(v.getColor());
            this.description2.setText(v.getDescription());
            int size = v.getSize();
            if(size == 1)
                pequeno2.doClick();
            else if(size==2)
                medio2.doClick();
            else //size == 3
                grande2.doClick();

            this.color2.setEditable(true);
            this.description2.setEditable(true);
        }


    }//GEN-LAST:event_board2ActionPerformed

