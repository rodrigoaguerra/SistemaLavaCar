    private void diagnosticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_diagnosticoActionPerformed
        //Se não tem nenhum item selecionado
        if(list.getSelectedIndex() == -1)
            JOptionPane.showMessageDialog(null, "Posição inválida. Selecione um veículo"
                    + " para fazer o diagnóstico", "Error", JOptionPane.ERROR_MESSAGE);
        else //Se tem algum selecionado
        {
            
            sugestoes.setText("");
            
            String placa = listModel.get(list.getSelectedIndex());
            Vehicle v = c.procuraVeiculo(placa);
            
            if(c.verificarMotor(v) == true)
                motor.setText("OK");
            else
            {
                motor.setText("FALHA NO MOTOR");
                sugestoes.setText(sugestoes.getText() + "Arrumar o motor\n");
            }
            if(c.verificarPneus(v) == true)
                pneus.setText("OK");
            else
            {
                pneus.setText("GASTOS");
                sugestoes.setText(sugestoes.getText() + "Trocar pneus\n");
            }
            if(c.verificarOleo(v) == true)
                oleo.setText("OK");
            else
            {
                oleo.setText("VAZANDO");
                sugestoes.setText(sugestoes.getText() + "Fixar o vazamento de óleo\n");
            }
            if(c.verificarTanque(v) == true)
                tanque.setText("CHEIO");
            else
                tanque.setText("COMB. ACABANDO");
            if(c.verificarCombustivel(v) == true)
                combustivel.setText("OK");
            else
            {
                combustivel.setText("VAZANDO");
                sugestoes.setText(sugestoes.getText() + "Fixar o vazamento de combustível\n");
            }
            if(c.verificarBateria(v) == true)
                bateria.setText("OK");
            else
            {
                bateria.setText("COM PROBLEMA");
                sugestoes.setText(sugestoes.getText() + "Arrumar a bateria\n");
            }
            if(c.verificarPolimento(v) == false)
                sugestoes.setText(sugestoes.getText() + "Polimento\n");
            if(c.verificarPintura(v) == false)
                sugestoes.setText(sugestoes.getText() + "Pintura\n");
            
            /* Muda os atributos. Seria o mesmo que consertar
            todos os problemas e fazer com que na próxima visita
            apareçam outros */
            v.randomizeState();
        }
    }//GEN-LAST:event_diagnosticoActionPerformed
        
